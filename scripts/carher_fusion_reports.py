def load_jsonl(path: Path) -> list[dict[str, Any]]:
    return [json.loads(line) for line in path.read_text().splitlines() if line.strip()]


def is_retryable_error(output: str) -> bool:
    try:
        data = json.loads(output)
    except json.JSONDecodeError:
        return False
    error = data.get("error") or {}
    return bool(error.get("retryable")) or error.get("subtype") == "rate_limit"


def run_json(cmd: list[str], cwd: Path | None = None, retries: int = 6) -> dict[str, Any]:
    delay = 5.0
    last_output = ""
    for attempt in range(retries + 1):
        proc = subprocess.run(cmd, text=True, capture_output=True, cwd=str(cwd) if cwd else None)
        last_output = proc.stderr or proc.stdout
        if proc.returncode == 0:
            data = json.loads(proc.stdout)
            if data.get("ok"):
                return data
            last_output = json.dumps(data, ensure_ascii=False)
        if attempt < retries and is_retryable_error(last_output):
            time.sleep(delay)
            delay = min(delay * 1.8, 60.0)
            continue
        raise RuntimeError(last_output)
    raise RuntimeError(last_output)


def selected_uids(args: argparse.Namespace) -> set[int] | None:
    values: set[int] = set()
    for item in args.uids or []:
        if "-" in item:
            start, end = item.split("-", 1)
            values.update(range(int(start), int(end) + 1))
        else:
            values.add(int(item))
    return values or None


def by_her_id(rows: list[dict[str, Any]]) -> dict[str, dict[str, Any]]:
    return {str(row.get("her_id")): row for row in rows if row.get("her_id")}


def e(text: Any) -> str:
    return html.escape(str(text), quote=True)


def pct(relative: dict[str, Any], name: str) -> str:
    item = relative.get(name) or {}
    rank = item.get("percentile_rank")
    tier = item.get("tier") or "no_signal"
    if rank is None:
        return tier
    return f"{tier} / P{round(float(rank) * 100)}"


def cell(value: Any) -> str:
    return f"<td>{e(value)}</td>"



def list_existing_records(base_token: str, table_id: str) -> dict[str, dict[str, Any]]:
    records: dict[str, dict[str, Any]] = {}
    offset = 0
    while True:
        data = run_json([
            "lark-cli",
            "base",
            "+record-list",
            "--base-token",
            base_token,
            "--table-id",
            table_id,
            "--offset",
            str(offset),
            "--limit",
            "200",
            "--format",
            "json",
            "--field-id",
            "Her ID",
            "--field-id",
            "报告链接",
        ])
        payload = data["data"]
        fields = payload["fields"]
        idx = {field: index for index, field in enumerate(fields)}
        for record_id, row in zip(payload["record_id_list"], payload["data"]):
            her_id_index = idx.get("Her ID")
            if her_id_index is None or her_id_index >= len(row) or not row[her_id_index]:
                continue
            records[str(row[her_id_index])] = {
                "record_id": record_id,
                "report_link": row[idx["报告链接"]] if idx.get("报告链接") is not None and idx["报告链接"] < len(row) else None,
            }
        if not payload.get("has_more"):
            break
        offset += 200
    return records


def write_reports(args: argparse.Namespace) -> dict[str, Any]:
    scores = load_jsonl(Path(args.scores))
    evidence = by_her_id(load_jsonl(Path(args.evidence)))
    score_summary = json.loads(Path(args.score_summary).read_text()) if args.score_summary else {}
    uid_filter = selected_uids(args)
    if uid_filter is not None:
        scores = [score for score in scores if int(score["uid"]) in uid_filter]
    if args.limit:
        scores = scores[: args.limit]

    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    report_dir = out_dir / "xml"
    report_dir.mkdir(parents=True, exist_ok=True)
    manifest_path = out_dir / "report-manifest.jsonl"
    existing_manifest: dict[str, dict[str, Any]] = {}
    if manifest_path.exists() and not args.force:
        existing_manifest = by_her_id(load_jsonl(manifest_path))
    current_manifest = dict(existing_manifest)

    table_records = list_existing_records(args.base_token, args.table_id) if args.sync_links else {}
    manifest_items: list[dict[str, Any]] = []
    for index, score in enumerate(scores, start=1):
        her_id = str(score["her_id"])
        xml = render_report(score, evidence.get(her_id, {}), score_summary)
        xml_path = report_dir / f"{her_id}-fusion-report.xml"
        xml_path.write_text(xml)

        prior = existing_manifest.get(her_id)
        url = prior.get("url") if prior else None
        document_id = prior.get("document_id") if prior else None
        created = False
        if args.create_docs and (args.force or not url):
            data = run_json([
                "lark-cli",
                "docs",
                "+create",
                "--api-version",
                "v2",
                "--content",
                f"@xml/{xml_path.name}",
                "--format",
                "json",
            ], cwd=out_dir)
            doc = data["data"]["document"]
            url = doc.get("url")
            document_id = doc.get("document_id")
            created = True
            if args.sleep_seconds and index < len(scores):
                time.sleep(args.sleep_seconds)

        synced = False
        if args.sync_links and url:
            record = table_records.get(her_id)
            if not record:
                raise RuntimeError(f"missing score table row for {her_id}")
            run_json([
                "lark-cli",
                "base",
                "+record-upsert",
                "--base-token",
                args.base_token,
                "--table-id",
                args.table_id,
                "--record-id",
                record["record_id"],
                "--json",
                json.dumps({"报告链接": url}, ensure_ascii=False),
                "--format",
                "json",
            ])
            synced = True

        item = {
            "uid": score["uid"],
            "her_id": her_id,
            "xml_path": str(xml_path),
            "url": url,
            "document_id": document_id,
            "created": created,
            "synced": synced,
        }
        current_manifest[her_id] = item
        manifest_path.write_text("".join(
            json.dumps(row, ensure_ascii=False, sort_keys=True) + "\n"
            for row in sorted(current_manifest.values(), key=lambda value: int(value.get("uid") or 0))
        ))
        manifest_items.append(item)
        print(json.dumps(item, ensure_ascii=False), flush=True)

    return {
        "reports": len(manifest_items),
        "created": sum(1 for item in manifest_items if item["created"]),
        "synced": sum(1 for item in manifest_items if item["synced"]),
        "manifest": str(manifest_path),
    }


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--evidence", required=True)
    parser.add_argument("--scores", required=True)
    parser.add_argument("--score-summary")
    parser.add_argument("--output-dir", required=True)
    parser.add_argument("--uids", nargs="*", help="UIDs or ranges, for example 3 10 20-30.")
    parser.add_argument("--limit", type=int)
    parser.add_argument("--create-docs", action="store_true")
    parser.add_argument("--sync-links", action="store_true")
    parser.add_argument("--force", action="store_true", help="Recreate docs even if manifest already has URLs.")
    parser.add_argument("--base-token", default=DEFAULT_BASE_TOKEN)
    parser.add_argument("--table-id", default=DEFAULT_TABLE_ID)
    parser.add_argument("--sleep-seconds", type=float, default=0.3)
    return parser.parse_args()


def main() -> int:
    result = write_reports(parse_args())
    print(json.dumps(result, ensure_ascii=False, indent=2, sort_keys=True), file=sys.stderr)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
